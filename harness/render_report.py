def _resolve_experiment_dir(arg: str) -> Path:
    p = Path(arg)
    if p.is_dir():
        return p.resolve()
    candidate = EXPERIMENTS_DIR / arg
    if candidate.is_dir():
        return candidate.resolve()
    raise SystemExit(f"Experiment directory not found: {arg}")


def main() -> int:
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "experiment", nargs="?", help="Path to an experiment dir, or experiment_id"
    )
    parser.add_argument(
        "--all", action="store_true",
        help="Render every experiment under experiments/",
    )
    parser.add_argument(
        "--force", action="store_true",
        help="Clobber the existing file (manual review section is lost)",
    )
    parser.add_argument(
        "--stdout", action="store_true",
        help="Print to stdout; do not write to disk",
    )
    args = parser.parse_args()

    if args.all:
        targets = sorted(p.parent for p in EXPERIMENTS_DIR.glob("*/result.json"))
        if not targets:
            print(f"[render_report] no experiments under {EXPERIMENTS_DIR}")
            return 0
    elif args.experiment:
        targets = [_resolve_experiment_dir(args.experiment)]
    else:
        parser.error("Provide an experiment path/id or --all")
        return 2

    failed = 0
    for d in targets:
        try:
            text = render_report(d, force=args.force)
        except (ResultValidationError, RuntimeError, FileNotFoundError) as e:
            failed += 1
            print(f"FAIL {d.name}: {e}", file=sys.stderr)
            continue
        if args.stdout:
            sys.stdout.write(text)
        else:
            (d / REPORT_FILENAME).write_text(text)
            print(f"OK   {d.name}/{REPORT_FILENAME}")
    return 1 if failed else 0


if __name__ == "__main__":
    sys.exit(main())
