#!/usr/bin/env python3
"""Render an experiment's artifacts into a human-readable Markdown report.

Usage:
    python harness/render_report.py <experiment-dir-or-id>
    python harness/render_report.py --all
    python harness/render_report.py <...> --stdout
    python harness/render_report.py <...> --force

Read-only over `result.json`, `transcript.md`, `diff.patch`. Writes
`experiments/<dir>/report.md`. The auto-rendered span sits between
`<!-- BEGIN AUTO -->` and `<!-- END AUTO -->` markers; everything outside
(including the user's "Manual review" section) is preserved across
re-renders byte-for-byte.

See docs/v1-tasks.md (T1.6) for the rationale and per-section spec.
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from dataclasses import dataclass
from pathlib import Path

import yaml

# Allow invocation as `python3 harness/render_report.py` (script mode), not
# just `python3 -m harness.render_report`. Mirrors run_experiment.py.
if __package__ in (None, ""):
    sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from harness.validate_result import ResultValidationError, validate_result  # noqa: E402

REPO_ROOT = Path(__file__).resolve().parent.parent
EXPERIMENTS_DIR = REPO_ROOT / "experiments"
TASKS_DIR = REPO_ROOT / "tasks" / "instances"

REPORT_FILENAME = "report.md"
BEGIN_MARKER = (
    "<!-- BEGIN AUTO — regenerated by harness/render_report.py; "
    "edits in this block are lost -->"
)
END_MARKER = "<!-- END AUTO -->"

MANUAL_REVIEW_TEMPLATE = """\
## Manual review

<!-- Free-form. Survives re-renders. Suggested prompts: -->

- What did the agent get right?
- What did it miss?
- Does the rubric score match your read of the deliverable?
- Calibration notes (anchors that need tightening, dimensions that saturated):
"""


# ---------------------------------------------------------------------------
# Diff parsing
# ---------------------------------------------------------------------------

@dataclass
class DiffFile:
    path: str
    kind: str  # 'new' | 'modified' | 'deleted' | 'binary'
    content: str | None
    raw_block: str


_DIFF_HEADER_RE = re.compile(r"^diff --git a/(.+?) b/(.+?)$", re.MULTILINE)


def parse_diff(diff_text: str) -> list[DiffFile]:
    """Split a unified-diff stream into per-file records."""
    if not diff_text.strip():
        return []
    blocks = re.split(r"(?m)^(?=diff --git )", diff_text)
    return [_parse_diff_block(b.rstrip("\n")) for b in blocks if b.strip()]


def _parse_diff_block(block: str) -> DiffFile:
    m = _DIFF_HEADER_RE.search(block)
    path = m.group(2) if m else "<unknown>"
    if re.search(r"^Binary files .* differ$", block, re.MULTILINE):
        return DiffFile(path=path, kind="binary", content=None, raw_block=block)
    if re.search(r"^new file mode ", block, re.MULTILINE):
        return DiffFile(
            path=path,
            kind="new",
            content=_restore_new_file(block),
            raw_block=block,
        )
    if re.search(r"^deleted file mode ", block, re.MULTILINE):
        return DiffFile(path=path, kind="deleted", content=None, raw_block=block)
    return DiffFile(path=path, kind="modified", content=None, raw_block=block)


def _restore_new_file(block: str) -> str:
    """Reconstruct the original file content from a new-file diff hunk."""
    lines = block.splitlines()
    hunk_start = None
    for i, line in enumerate(lines):
        if line.startswith("@@"):
            hunk_start = i + 1
            break
    if hunk_start is None:
        return ""

    restored: list[str] = []
    no_trailing_newline = False
    for line in lines[hunk_start:]:
        if line.startswith("+"):
            restored.append(line[1:])
        elif line.startswith("\\ No newline at end of file"):
            no_trailing_newline = True

    text = "\n".join(restored)
    if restored and not no_trailing_newline:
        text += "\n"
    return text


# ---------------------------------------------------------------------------
# Transcript prose extraction
# ---------------------------------------------------------------------------

_RESULT_HEADER_RE = re.compile(r"(?m)^##\s+result\b")


def extract_result_section(transcript_md: str) -> str | None:
    """Return the body of the `## result` H2 section, or None if absent."""
    match = _RESULT_HEADER_RE.search(transcript_md)
    if not match:
        return None
    after = transcript_md[match.end():]
    next_h2 = re.search(r"(?m)^##\s+", after)
    body = after[: next_h2.start()] if next_h2 else after
    return body.strip("\n")


