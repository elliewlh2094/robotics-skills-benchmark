def render_identity(result: dict) -> str:
    plugin_lines = [f"- **plugin_tag:** `{result.get('plugin_tag')}`"]
    if result.get("plugin_sha"):
        plugin_lines.append(f"- **plugin_sha:** `{result['plugin_sha']}`")
    if result.get("plugin_repo"):
        plugin_lines.append(f"- **plugin_repo:** {result['plugin_repo']}")
    if result.get("plugin_ref"):
        plugin_lines.append(f"- **plugin_ref:** `{result['plugin_ref']}`")
    if result.get("plugin_path"):
        plugin_lines.append(f"- **plugin_path:** `{result['plugin_path']}`")

    run_lines = [
        f"- **experiment_id:** `{result.get('experiment_id')}`",
        f"- **task_id:** `{result.get('task_id')}`",
        f"- **run_id:** `{result.get('run_id')}`",
        f"- **base_repo:** {result.get('base_repo')}",
        f"- **base_sha:** `{result.get('base_sha')}`",
    ]

    exec_lines = [
        f"- **status:** `{result.get('status')}`",
        f"- **started_at:** {result.get('started_at')}",
        f"- **completed_at:** {result.get('completed_at')}",
    ]
    if isinstance(result.get("runtime_s"), (int, float)):
        exec_lines.append(f"- **runtime_s:** {result['runtime_s']:.2f}")
    else:
        exec_lines.append(f"- **runtime_s:** {result.get('runtime_s')}")
    exec_lines.append(f"- **exit_code:** {result.get('exit_code')}")
    if result.get("error"):
        exec_lines.append(f"- **error:** {result['error']}")

    config_lines = [
        f"- **schema_version:** {result.get('schema_version')}",
        f"- **max_turns:** {result.get('max_turns')}",
        f"- **seed:** {result.get('seed')}",
    ]
    if result.get("available_tools"):
        config_lines.append(
            "- **available_tools:** "
            + ", ".join(f"`{t}`" for t in result["available_tools"])
        )
    if result.get("scope_files_declared"):
        config_lines.append(
            "- **scope_files_declared:** "
            + ", ".join(f"`{p}`" for p in result["scope_files_declared"])
        )
    if result.get("hook_blocks") is not None:
        config_lines.append(f"- **hook_blocks:** {result['hook_blocks']}")

    output_lines = []
    fm = result.get("files_modified") or []
    if fm:
        output_lines.append(
            "- **files_modified:** " + ", ".join(f"`{p}`" for p in fm)
        )
    else:
        output_lines.append("- **files_modified:** _(none)_")
    if result.get("judge_calls") is not None:
        output_lines.append(f"- **judge_calls:** {result['judge_calls']}")
    if result.get("transcript_bytes") is not None:
        output_lines.append(f"- **transcript_bytes:** {result['transcript_bytes']}")

    sources = (
        "- **Source files (relative to this report):** `result.json`, "
        "`transcript.md`, `diff.patch`"
    )

    return "\n".join(
        [
            "## Run identity",
            "",
            "**Plugin**",
            *plugin_lines,
            "",
            "**Run**",
            *run_lines,
            "",
            "**Execution**",
            *exec_lines,
            "",
            "**Config**",
            *config_lines,
            "",
            "**Outputs**",
            *output_lines,
            "",
            sources,
        ]
    )


def render_task_definition(result: dict, tasks_dir: Path) -> str:
    """Show the prompt the agent saw, plus scope/tools/verification metadata.

    The `problem_statement` is the dominant cause of agent output, so it
    must be visible alongside the deliverable when reviewers calibrate the
    rubric. If `tasks/instances/<task_id>/task.yaml` is missing, emit a `>`
    warning and continue — the rest of the report should still render.
    """
    header = "## Task definition"
    task_id = result.get("task_id")
    if not task_id:
        return f"{header}\n\n> _`task_id` missing from result.json._"

    task_path = tasks_dir / task_id / "task.yaml"
    if not task_path.exists():
        return (
            f"{header}\n\n"
            f"> _Task definition not found at "
            f"`tasks/instances/{task_id}/task.yaml`._"
        )

    try:
        with task_path.open() as f:
            task = yaml.safe_load(f) or {}
    except yaml.YAMLError as e:
        return f"{header}\n\n> _Failed to parse `{task_path.name}`: {e}._"

    problem = (task.get("problem_statement") or "").rstrip()
    scope_files = task.get("scope_files") or []
    available_tools = task.get("available_tools") or []
    verification_method = task.get("verification_method") or "—"

    lines = [header, "", "**Problem statement (verbatim from task.yaml):**", ""]
    if problem:
        lines.append(_fenced(problem + "\n", "txt"))
    else:
        lines.append("> _`problem_statement` missing or empty._")
    lines.extend(
        [
            "",
            "**Scope and tooling**",
            "",
            "- **scope_files:** "
            + (", ".join(f"`{p}`" for p in scope_files) if scope_files else "_(none)_"),
            "- **available_tools:** "
            + (
                ", ".join(f"`{t}`" for t in available_tools)
                if available_tools
                else "_(none)_"
            ),
            f"- **verification_method:** `{verification_method}`",
        ]
    )
    return "\n".join(lines)


_INSTRUMENT_WARNING = (
    "> ⚠ **Measurement-instrument warning:** this rubric grades as saturated "
    "on this run — every dimension's mean is 3.0 with zero pooled stdev. A "
    "perfect mean with zero variance means the instrument cannot detect "
    "plugin improvement (only degradation). Treat the score as a non-result; "
    "consult the calibration log in `analysis/baseline-v0.1.0.md`."
)

_INSTRUMENT_HEALTHY = (
    "Instrument health: at least one dimension shows non-zero variance or "
    "below-ceiling mean — comparison with v0.1.0 baseline is meaningful."
)


def render_instrument_health(scoring: dict) -> str:
    """Auto-flag the saturated-rubric case so weak baselines are visible.

    Returns the empty string when no rubric data is present (e.g.,
    `status: "no-deliverable"` skips the judge); the section is then absent
    from the report rather than emitting a misleading "healthy" claim.
    """
    rs = scoring.get("rubric_scores") or {}
    mean = rs.get("mean") or {}
    stdev = rs.get("stdev") or {}
    if not mean or not stdev:
        return ""

    saturated = all(
        isinstance(mean.get(d), (int, float))
        and isinstance(stdev.get(d), (int, float))
        and abs(mean[d] - 3.0) < 1e-9
        and abs(stdev[d]) < 1e-9
        for d in mean
    )

    body = _INSTRUMENT_WARNING if saturated else _INSTRUMENT_HEALTHY
    return f"## Measurement instrument health\n\n{body}"


def render_transcript(transcript_md: str | None, transcript_path: Path) -> str:
    if transcript_md is None:
        return (
            "## Agent transcript\n\n"
            f"> _Source file not found at `{transcript_path.name}`._"
        )
    body = extract_result_section(transcript_md)
    if body is None:
        return (
            "## Agent transcript\n\n"
            f"> _Renderer could not locate `## result` in `{transcript_path.name}`; "
            "see raw file._"
        )
    return f"## Agent transcript\n\n{body}"


def render_deliverable(
    diff_text: str | None, diff_path: Path, result: dict
) -> str:
    header = "## Restored deliverable"
    if diff_text is None:
        return f"{header}\n\n> _Source file not found at `{diff_path.name}`._"

    files_modified = result.get("files_modified") or []
    out_of_scope = set(
        result.get("scoring", {})
        .get("scope_check", {})
        .get("out_of_scope_paths", [])
    )

    if not files_modified and result.get("status") == "no-deliverable":
        return (
            f"{header}\n\n"
            "> _No in-scope files were produced; status=`no-deliverable`._"
        )
    if not files_modified:
        return f"{header}\n\n> _No files modified._"

    parsed = {df.path: df for df in parse_diff(diff_text)}
    in_scope = sorted(p for p in files_modified if p not in out_of_scope)

    sections = [header, ""]
    for path in in_scope:
        df = parsed.get(path)
        sections.append(f"### `{path}`")
        sections.append("")
        if df is None:
            sections.append(
                f"> _Path `{path}` listed in `files_modified` but not found "
                "in `diff.patch`._"
            )
            sections.append("")
            continue
        if df.kind == "new":
            sections.append(_fenced(df.content or "", path))
        elif df.kind == "modified":
            sections.append(
                "> _File was modified, not newly created. "
                "Showing patch hunk verbatim._"
            )
            sections.append("")
            sections.append(_fenced(df.raw_block, "diff"))
        elif df.kind == "deleted":
            sections.append("> _File was deleted in this experiment._")
        elif df.kind == "binary":
            sections.append(f"> _`{path}` (binary, not reconstructed)._")
        sections.append("")

    if out_of_scope:
        sections.append("### Out-of-scope files (not restored above)")
        sections.append("")
        for path in sorted(out_of_scope):
            sections.append(f"- `{path}`")

    return "\n".join(sections).rstrip() + "\n"


def _fenced(content: str, lang_or_path: str) -> str:
    """Wrap content in a fenced code block, escalating fence length if needed."""
    fence = "```"
    while fence in content:
        fence += "`"
    lang = ""
    if lang_or_path == "diff":
        lang = "diff"
    elif lang_or_path.endswith(".md"):
        lang = "markdown"
    elif lang_or_path.endswith(".py"):
        lang = "python"
    elif lang_or_path.endswith((".yaml", ".yml")):
        lang = "yaml"
    suffix = "" if content.endswith("\n") else "\n"
    return f"{fence}{lang}\n{content}{suffix}{fence}"


def render_rubric(scoring: dict) -> str:
    rs = scoring.get("rubric_scores") or {}
    if "per_trial" not in rs:
        return (
            "## Rubric scores\n\n"
            "> _Rubric scoring did not produce per-trial data; "
            "see `result.json` for the error record._"
        )

    per_trial = rs.get("per_trial", [])
    mean = rs.get("mean", {})
    stdev = rs.get("stdev", {})
    n_trials = rs.get("n_trials", len(per_trial))
    overall_mean = rs.get("overall_mean")
    overall_stdev = rs.get("overall_stdev")

    dims = sorted(
        set(mean.keys()) | {k for t in per_trial for k in t.get("scores", {})}
    )
    trial_cols = [f"trial_{i+1}" for i in range(n_trials)]

    header = ["dimension", "mean", "stdev", *trial_cols]
    rows = [header, ["---"] * len(header)]
    for d in dims:
        m = mean.get(d)
        s = stdev.get(d)
        trials = [t.get("scores", {}).get(d, "—") for t in per_trial]
        rows.append(
            [
                d,
                f"{m:.2f}" if isinstance(m, (int, float)) else "—",
                f"{s:.4f}" if isinstance(s, (int, float)) else "—",
                *[str(v) for v in trials],
            ]
        )

    table = "\n".join("| " + " | ".join(r) + " |" for r in rows)

    overall_mean_s = (
        f"{overall_mean:.2f}" if isinstance(overall_mean, (int, float)) else "—"
    )
    overall_stdev_s = (
        f"{overall_stdev:.4f}"
        if isinstance(overall_stdev, (int, float))
        else "—"
    )

    lines = [
        "## Rubric scores",
        "",
        table,
        "",
        f"**Overall:** mean = {overall_mean_s}, "
        f"stdev = {overall_stdev_s}, n_trials = {n_trials}",
    ]

    sc = scoring.get("scope_check") or {}
    oos_count = sc.get("out_of_scope_file_count", 0)
    oos_paths = sc.get("out_of_scope_paths", [])
    lines.extend(
        [
            "",
            "**Scope check**",
            "",
            f"- out_of_scope_file_count: {oos_count}",
        ]
    )
    if oos_paths:
        lines.append("- out_of_scope_paths:")
        for p in oos_paths:
            lines.append(f"  - `{p}`")
    else:
        lines.append("- out_of_scope_paths: _(none)_")

    return "\n".join(lines)


def render_rationales(scoring: dict) -> str:
    per_trial = (scoring.get("rubric_scores") or {}).get("per_trial") or []
    if not per_trial:
        return "## Judge rationales\n\n> _No per-trial rationales recorded._"
    parts = ["## Judge rationales", ""]
    for i, t in enumerate(per_trial, start=1):
        recomputed = t.get("overall_recomputed")
        reported = t.get("overall_judge_reported")
        rationale = t.get("rationale", "")
        recomputed_s = (
            f"{recomputed:.2f}" if isinstance(recomputed, (int, float)) else "—"
        )
        reported_s = (
            f"{reported}" if reported is not None else "—"
        )
        parts.append(
            f"### Trial {i} "
            f"(overall_recomputed={recomputed_s}, "
            f"judge_reported={reported_s})"
        )
        parts.append("")
        parts.append(rationale.strip())
        parts.append("")
    return "\n".join(parts).rstrip()


# ---------------------------------------------------------------------------
# Auto-block + report assembly
# ---------------------------------------------------------------------------

def render_auto_block(
    experiment_dir: Path, *, tasks_dir: Path = TASKS_DIR
) -> str:
    """Build the deterministic auto-rendered span (between BEGIN/END markers).

    `tasks_dir` is overridable so tests can inject a fake `task.yaml`
    location (e.g., to verify the missing-file path).
    """
    result_path = experiment_dir / "result.json"
    transcript_path = experiment_dir / "transcript.md"
    diff_path = experiment_dir / "diff.patch"

    with result_path.open() as f:
        result = json.load(f)

    try:
        validate_result(result)
    except ResultValidationError as e:
        raise ResultValidationError(
            f"{result_path} fails schema validation; "
            f"run `python harness/validate_result.py {result_path}` to inspect: {e}"
        )

    transcript_md = (
        transcript_path.read_text() if transcript_path.exists() else None
    )
    diff_text = diff_path.read_text() if diff_path.exists() else None

    scoring = result.get("scoring", {})
    sections = [
        render_identity(result),
        render_task_definition(result, tasks_dir),
        render_transcript(transcript_md, transcript_path),
        render_deliverable(diff_text, diff_path, result),
        render_rubric(scoring),
        render_instrument_health(scoring),
        render_rationales(scoring),
    ]
    sections = [s for s in sections if s]
    return "\n\n".join(sections).rstrip() + "\n"


def compose_report(
    auto_block: str,
    experiment_id: str,
    existing: str | None,
    *,
    force: bool,
) -> str:
    """Merge a freshly-rendered auto-block into existing report content.

    If `existing` is None or `force` is True, returns a fresh report from
    the template. Otherwise, locates the BEGIN/END markers in `existing`
    and rewrites only the span between them.
    """
    if existing is None or force:
        return _new_report(auto_block, experiment_id)

    if BEGIN_MARKER not in existing or END_MARKER not in existing:
        raise RuntimeError(
            "report.md exists but is missing BEGIN/END AUTO markers; "
            "use --force to regenerate (this clobbers manual edits)."
        )

    pattern = re.compile(
        re.escape(BEGIN_MARKER) + r"\n.*?\n" + re.escape(END_MARKER),
        re.DOTALL,
    )
    replacement = f"{BEGIN_MARKER}\n\n{auto_block}\n{END_MARKER}"
    return pattern.sub(lambda _m: replacement, existing, count=1)


def _new_report(auto_block: str, experiment_id: str) -> str:
    return (
        f"# Experiment report: `{experiment_id}`\n\n"
        f"{BEGIN_MARKER}\n\n"
        f"{auto_block}\n"
        f"{END_MARKER}\n\n"
        f"{MANUAL_REVIEW_TEMPLATE}"
    )


def render_report(
    experiment_dir: Path,
    *,
    force: bool = False,
    tasks_dir: Path = TASKS_DIR,
) -> str:
    """High-level: produce the full report text for an experiment dir."""
    auto = render_auto_block(experiment_dir, tasks_dir=tasks_dir)
    experiment_id = experiment_dir.name
    report_path = experiment_dir / REPORT_FILENAME
    existing = report_path.read_text() if report_path.exists() else None
    return compose_report(auto, experiment_id, existing, force=force)


